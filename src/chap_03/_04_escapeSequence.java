package chap_03;

public class _04_escapeSequence {
    public static void main(String[] args) {
        // 특수문자, 이스케이프 문자 (Escape Sequence, Escape Character, Special Character)
        System.out.println("자바가\n매우\n재밍ㅆ어요");
        System.out.println("자바가\t매우\t재밌어요");

        System.out.println("다섯글자요\t구르트");
        System.out.println("세글자\t구르트");

        System.out.println("C:\\Program Files\\Java");
        System.out.println("\"나는 고양이로소이다. \"");

        System.out.println("abcd\be");
        System.out.println("abcde\rfg");
    }
}
